# Yamaoka Shiro's wise saying.

def dekisokonai(butabara="豚バラ煮込み"):
    return f"この{butabara}は出来損ないだ、食べられないよ"

def kokkei(shokutu="日本の食通"):
    return f"{shokutu}とたてまつられる人間は滑稽だねぇ！"

def sushi(neta="ネタ", shari="シャリ", oyaji="オヤジ"):
    return f"{neta}は最高、{shari}も最高、だが{oyaji}、肝心のおまえの腕が最低だ。"

def junkin(junkin="純金の鍋", narikin="成金"):
    return f"気のせいではありませんよ、{junkin}なんぞを作って喜んでいるただの{narikin}から、成長した証拠です。"

def umai_mazui(umai="旨い", mazui="まずい", shita="舌"):
    return f"{umai}か{mazui}か自分の{shita}で判断したらどうなんだ！！"

def asu_mouichido(something="寿司"):
    return f"明日もう一度ここに来てください。本物の{something}というものをお見せしますよ。"