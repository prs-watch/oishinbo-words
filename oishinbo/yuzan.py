# Kaibara Yuzan's wise saying.

def kuruma(kuruma="車"):
    return f"馬鹿どもに{kuruma}を与えるなっ！"

def arai(arai="あらい"):
    return f"この{arai}を作ったのは誰だあっ！"

def kubi():
    return f"きさまか！きさまはクビだ、出て行けっ！"

def hiyashi_chuka(hiyashi="冷やし中華"):
    return f"{hiyashi}だとっ！？ふざけるなあっ！"

def okami(okami="女将"):
    return f"{okami}を呼べっ！"